CREATE TABLE ISTITUTI AS
SELECT row_number() OVER (ORDER BY ANNOSCOLASTICO, REGIONE, PROVINCIA, CODICEISTITUTORIFERIMENTO) IstitutoId, 
       ANNOSCOLASTICO, 
       AREAGEOGRAFICA, 
       REGIONE, 
       PROVINCIA, 
       CODICEISTITUTORIFERIMENTO, 
       DENOMINAZIONEISTITUTORIFERIMENTO, 
       COUNT(*) TOTALESCUOLE 
  FROM SCUOLE_RAW
GROUP BY ANNOSCOLASTICO, AREAGEOGRAFICA, REGIONE, PROVINCIA, CODICEISTITUTORIFERIMENTO, DENOMINAZIONEISTITUTORIFERIMENTO
ORDER BY ANNOSCOLASTICO, REGIONE, PROVINCIA, CODICEISTITUTORIFERIMENTO;

CREATE TABLE SCUOLE AS
SELECT row_number() OVER (ORDER BY ANNOSCOLASTICO, REGIONE, PROVINCIA, CODICEISTITUTORIFERIMENTO, CODICESCUOLA) ScuolaId, 
       row_number() OVER (ORDER BY ANNOSCOLASTICO, REGIONE, PROVINCIA, CODICEISTITUTORIFERIMENTO) IstitutoId, 
       CODICEISTITUTORIFERIMENTO, 
       CODICESCUOLA, 
       DENOMINAZIONESCUOLA, 
       INDIRIZZOSCUOLA, 
       CAPSCUOLA, 
       CODICECOMUNESCUOLA, 
       DESCRIZIONECOMUNE, 
       REGIONE, 
       PROVINCIA, 
       DESCRIZIONECARATTERISTICASCUOLA, 
       DESCRIZIONETIPOLOGIAGRADOISTRUZIONESCUOLA
  FROM SCUOLE_RAW
ORDER BY ANNOSCOLASTICO, REGIONE, PROVINCIA, CODICEISTITUTORIFERIMENTO, CODICESCUOLA;